Class.Inherits(TCollection, TBase);
function TCollection(itemClass) {
	TCollection.prototype.parent.call(this, itemClass);
	
	this.itemClass = itemClass; // default item class
};

TCollection.prototype.add = function(itemClass) {
	return new this.itemClass();
};
